package pkmn;

/**
 * Represents various energy types that can be associated with a card in the context of a card game.
 * Each energy type corresponds to a specific kind of energy that can be used to perform actions or attacks.
 */
public enum EnergyType {
    FIRE,
    GRASS,
    WATER,
    LIGHTNING,
    PSYCHIC,
    FIGHTING,
    DARKNESS,
    METAL,
    FAIRY,
    DRAGON,
    COLORLESS
}
